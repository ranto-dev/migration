-- ======================================================
--      Création des tables Oracle pour ECommerceDB
-- ======================================================

-- TABLE CLIENTS
CREATE TABLE CLIENTS (
    ID_CLIENT NUMBER PRIMARY KEY,
    NOM VARCHAR2(50),
    PRENOM VARCHAR2(50),
    EMAIL VARCHAR2(100),
    TELEPHONE VARCHAR2(20)
);

-- TABLE PRODUITS
CREATE TABLE PRODUITS (
    ID_PRODUIT NUMBER PRIMARY KEY,
    NOM_PRODUIT VARCHAR2(100),
    PRIX NUMBER(10,2),
    CATEGORIE VARCHAR2(50)
);

-- TABLE COMMANDES
CREATE TABLE COMMANDES (
    ID_COMMANDE NUMBER PRIMARY KEY,
    ID_CLIENT NUMBER REFERENCES CLIENTS(ID_CLIENT),
    DATE_COMMANDE DATE,
    TOTAL NUMBER(10,2)
);

-- TABLE PAIEMENTS
CREATE TABLE PAIEMENTS (
    ID_PAIEMENT NUMBER PRIMARY KEY,
    ID_COMMANDE NUMBER REFERENCES COMMANDES(ID_COMMANDE),
    MONTANT NUMBER(10,2),
    MODE_PAIEMENT VARCHAR2(50),
    DATE_PAIEMENT DATE
);

-- TABLE DETAILS_COMMANDE
CREATE TABLE DETAILS_COMMANDE (
    ID_DETAIL NUMBER PRIMARY KEY,
    ID_COMMANDE NUMBER REFERENCES COMMANDES(ID_COMMANDE),
    ID_PRODUIT NUMBER REFERENCES PRODUITS(ID_PRODUIT),
    QUANTITE NUMBER
);

